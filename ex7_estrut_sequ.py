# área do quadrado

ent1= float(input('Entre a medida do base do quadrado: '))
ent2= float(input('Entre a medida da altura do quadrado: '))
aq=ent1*ent2
aqr=round(aq,2)
print('A medida do quadrado é ',aqr)
print('O dobre da área é ',aqr*2)
